# CVE analysis

# Usage context:
#   - Which component/package is vulnerable? affected versions / version ranges?
#   - When, where, how is it used? direct or transitive dependency?
#   - Which part of component is vulnerable? What kind of vulnerability?
#   - List of CVEs, environmental CVSS scores and vectors
#   - Overall assessment: HIGH, MEDIUM, LOW, Not Applicable

# Probability:
#   - Level of probability
#   - Prerequisites/preconditions — auth required? specific config flag enabled? non-default build option?
#   - The conditions to exploit, the exploit paths
#   - How difficult of those paths? Public exploit availability?

# Impacts:
#   - Level of impact
#   - Which aspects it impacts? Confidentiality? Integrity? Availability?
#   - Does the scope change?
#   - Which features, components, information would be impacted?
#   - Which kind of sensitive data would be impacted?

# Mitigation:
#   - Which version of product will include the fixes?